#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		i;
	char	c[12];
	int		p;

	p = 0;
	if (nb == -2147483648)
		write (1, "-2147483648", 11);
	if (nb < 0 && nb != -2147483648)
	{
		write (1, "-", 1);
		nb = -nb;
	}
	if (nb == 0)
		write (1, "0", 1);
	while (nb != 0 && nb != -2147483648)
	{
		i = nb % 10;
		nb = nb / 10;
		c[p] = '0' + i;
		p++;
	}
	c [p] = '\0';
	while (--p >= 0)
		write (1, &c[p], 1);
}
